package enzansi;

/**
 * 実力確認問題　2．演算子　4
 */
 public class Enzansi4 {
	 /**
	  * main メソッド
	  * int 型の変数 x に数値 7 を代入。
	  * 変数 x の値を 3 倍した値を表示。
	  * 変数 x の値を半分(1/2)にした値を表示。
	  * @param args 実行引数
	  */
	 public static void main(String[]args) {
		 int x = 7;
		 x = x * 3;
		 System.out.println(x);
		 x = x / 2; 
		 System.out.println(x);
	 }
 }